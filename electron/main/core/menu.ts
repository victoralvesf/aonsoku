import { platform } from '@electron-toolkit/utils'
import { app, Menu, shell } from 'electron'
import { repository } from '../../../package.json'
import { aboutDialog } from './about'

export function createAppMenu() {
  if (!platform.isMacOS) return

  const template = [
    {
      label: app.name,
      submenu: [
        {
          label: `About ${app.name}`,
          click: () => aboutDialog(),
        },
        { type: 'separator' },
        { role: 'services' },
        { type: 'separator' },
        { role: 'hide' },
        { role: 'hideOthers' },
        { role: 'unhide' },
        { type: 'separator' },
        { role: 'quit' },
      ],
    },
    {
      label: 'File',
      submenu: [{ role: 'close' }],
    },
    {
      label: 'Edit',
      submenu: [
        { role: 'undo' },
        { role: 'redo' },
        { type: 'separator' },
        { role: 'cut' },
        { role: 'copy' },
        { role: 'paste' },
        { role: 'delete' },
        { role: 'selectAll' },
        { type: 'separator' },
        {
          label: 'Speech',
          submenu: [{ role: 'startSpeaking' }, { role: 'stopSpeaking' }],
        },
      ],
    },
    {
      label: 'View',
      submenu: [
        { role: 'reload' },
        { role: 'forceReload' },
        { role: 'toggleDevTools' },
        { type: 'separator' },
        { role: 'resetZoom' },
        { role: 'zoomIn' },
        { role: 'zoomOut' },
        { type: 'separator' },
        { role: 'togglefullscreen' },
      ],
    },
    {
      label: 'Window',
      submenu: [
        { role: 'minimize' },
        { role: 'zoom' },
        { type: 'separator' },
        { role: 'front' },
        { type: 'separator' },
        { role: 'window' },
      ],
    },
    {
      role: 'help',
      submenu: [
        {
          label: 'Github',
          click: async () => {
            await shell.openExternal(repository.url)
          },
        },
      ],
    },
  ]

  // @ts-expect-error set only roles for menu
  const menu = Menu.buildFromTemplate(template)

  Menu.setApplicationMenu(menu)
}
